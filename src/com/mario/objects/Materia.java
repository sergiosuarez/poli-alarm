package com.mario.objects;

public class Materia {
	public String ma_id;
	public String ma_nombre;
	public String ma_aula;
	public String ma_estado;
	public int year;
	public int month;
	public int day;
	public int hour;
	public int minute;
	
	public String getMa_id() {
		return ma_id;
	}
	public void setMa_id(String ma_id) {
		this.ma_id = ma_id;
	}
	public String getMa_nombre() {
		return ma_nombre;
	}
	public void setMa_nombre(String ma_nombre) {
		this.ma_nombre = ma_nombre;
	}
	public String getMa_aula() {
		return ma_aula;
	}
	public void setMa_aula(String ma_aula) {
		this.ma_aula = ma_aula;
	}
	public String getMa_estado() {
		return ma_estado;
	}
	public void setMa_estado(String ma_estado) {
		this.ma_estado = ma_estado;
	}
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
	public int getMonth() {
		return month;
	}
	public void setMonth(int month) {
		this.month = month;
	}
	public int getDay() {
		return day;
	}
	public void setDay(int day) {
		this.day = day;
	}
	public int getHour() {
		return hour;
	}
	public void setHour(int hour) {
		this.hour = hour;
	}
	public int getMinute() {
		return minute;
	}
	public void setMinute(int minute) {
		this.minute = minute;
	}
	
	
	
}
